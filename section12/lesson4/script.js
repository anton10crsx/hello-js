console.log("=== for ===");

for (let i = 1; i <= 10; i++) {
  console.log(i);
}

// - - -

console.log("=== while ===");

let i = 1;

while (i <= 10) {
  console.log(i);
  i++;
}

// - - -

console.log("=== do ===");

let j = 1;

do {
  console.log(j);
  j++;
} while (j <= 10); // -10;

